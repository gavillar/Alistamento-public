import Foundation
import UIKit

extension Create {
    
    static func labelTitle() -> UILabel {
       let label = UILabel()
        label.translatesAutoresizingMaskIntoConstraints = false
        label.backgroundColor = .black

        return label
    }
    
    
    
}
